/*
 * File: routesConfigs
 * Desc: contains all routes in app
 */

import React from 'react'
import {Redirect} from 'react-router-dom'
import requireGuest from '../utils/requireGuest'

import {
    /*
     |--------------------------------------------------------------------------
     |  Landing
     |--------------------------------------------------------------------------
     */
    Home,

    /*
     |--------------------------------------------------------------------------
     |  Authenticate
     |--------------------------------------------------------------------------
     */
    Login,
    ForgotPassword,

    /*
     |--------------------------------------------------------------------------
     |  Dashboard
     |--------------------------------------------------------------------------
     */
    Dashboard,

    /*
     |--------------------------------------------------------------------------
     |  Other
     |--------------------------------------------------------------------------
     */
    NotFound
} from '../containers';

export default {
    /*
     |--------------------------------------------------------------------------
     |  Landing
     |--------------------------------------------------------------------------
     */
    home: {
        path: '/',
        component: Home,
        exact: true
    },

    /*
     |--------------------------------------------------------------------------
     |  Authenticate
     |--------------------------------------------------------------------------
     */
    login: {
        path: '/login',
        component: requireGuest(Login)
    },
    forgotPass: {
        path: '/forgot-password',
        component: ForgotPassword
    },

    /*
     |--------------------------------------------------------------------------
     |  Dashboard
     |--------------------------------------------------------------------------
     */
    dashboard: {
        path: '/dashboard',
        component: Dashboard
    },

    /*
     |--------------------------------------------------------------------------
     |  Other
     |--------------------------------------------------------------------------
     */
    notFound: {
        path: '/page-not-found',
        component: NotFound
    },
    others: {
        component: () => <Redirect to='/page-not-found'/>
    }
}